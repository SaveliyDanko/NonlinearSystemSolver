def show_nonlinear_equation_methods():
    """
    Меню выбора метода решения для нелинейного уравнения.
    Возвращает выбранный метод (строку).
    """
    print("\nВыберите метод решения нелинейного уравнения:")
    print("1) Метод хорд")
    print("2) Метод Ньютона")
    print("3) Метод простых итераций")

    while True:
        choice = input("Введите номер метода (или 'q' для отмены): ").strip()

        if choice.lower() == 'q':
            print("Отмена выбора метода.")
            return None

        if choice in ['1', '2', '3']:
            return choice
        else:
            print("Некорректный ввод. Попробуйте снова.")


